sub makeverts
{
  $midx=$midy=$midz=0;
  $count=0;
  foreach(split("\n",$_[0]))
  {
    ($a,$x,$y,$z)=split(' ',$_);
    $x=int($x*64) + 3;
    $y=int($y*64) - 80;
    $z=int($z*64) + 1;
    print "$x,$y,$z,\n";
    $midx+=$x;
    $midy+=$y;
    $midz+=$z;
    $count++;
  }
  print $midx/$count," ...\n";
  print $midy/$count," ...\n";
  print $midz/$count," ...\n";
}

